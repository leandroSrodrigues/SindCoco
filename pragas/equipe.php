								<p><b>MONITOR DE CAMPO:</b> (Empregado de campo com o 1º grau completo) -  Pessoa encarregada da fiscalização fitossanitária da plantação. Sua função principal na equipe é percorrer as fileiras de plantas, predeterminadas, anotando nas fichas de campo a presença/ausência de pragas, de acordo com o método de fiscalização utilizado e com a natureza do problema. No caso de não saber identificar uma anomalia, deve assinalar a provável causa com um ponto de interrogação e levar no final do dia de trabalho ao conhecimento do coordenador do serviço de fiscalização (PM? – Planta morta – causa desconhecida; PA? – Plata com amarelecimento suspeito, etc.). É sua função também, a cada período de fiscalização, totalizar os dados das parcelas sob sua responsabilidade e repassa-los, sem rasuras, para o Caderno de Campo.</p>

