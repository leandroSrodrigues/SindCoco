									<h4>Tomada de Decisão</h4>
									<br>
								

									<p>Cabe ao gerente, ou ao proprietário, decidir sobre a medida mais adequada para solucionar um problema surgido na propriedade, que, simultaneamente, reduza custos e maximize benefícios. De forma simplista, a tomada de decisão apresenta três estágios: i) identificação e definição do problema com base no monitoramento da plantação; ii) geração de alternativas de solução de acordo com o conhecimento da relação entre fatores bióticos e abióticos e o meio ambiente; e iii) seleção e implementação da solução, representada pela adoção ou adaptação das medidas de controle disponíveis.</p>

									<p>Em determinadas situações, pode-se não dispor de solução para o problema identificado (novas pragas e doenças). Nesse caso, o gerente precisa ser ágil o suficiente para encontrar uma solução adequada ou para adaptar uma solução disponível às mudanças de ambiente, ou seja, lidar a tempo com situações imprevistas. Esse processo não deve nunca basear-se em regras e normas, mas no julgamento, na intuição, na criatividade, na liderança e no poder dado ao gerente para resolver problemas frequentes, esporádicos e/ou novos. Em hipótese alguma, uma decisão voltada para a proteção do plantio poderá ser considerada de forma isolada do contexto geral da atividade da propriedade.</p>

									<p>A prática de inspecionar regularmente a plantação (monitoramento) ajuda muito o gerente no momento da tomada de decisão. Essa prática permite detectar e identificar problemas de pragas e doenças tão logo apareçam na área, avaliar o grau de infestação/infecção causado às plantas ou à plantação e a importância econômica dos danos gerados. A depender do resultado do campo, a decisão pode ser: a) continuar a inspecionar a área sob monitoramento em intervalos menores para avaliar a evolução da infestação; b) estabelecer o momento apropriado para a intervenção; e c) escolher os meios mais adequados, eficientes e seguros de controle.</p>

									<p>A ocorrência de uma espécie-praga pode ser tolerada na plantação até determinado nível de dano sem resultar em prejuízos econômicos. Por isso, no manejo de uma plantação de coqueiro, é importante que os gerentes, ou os técnicos encarregados de assegurar a produção, tenham um bom conhecimento dos problemas fitossanitários, afins à cultura, do grau de tolerância da plantação à presença das espécies e suas respectivas injúrias e do conjunto de medidas de controle que podem ser utilizadas no manejo da plantação, de forma integrada, com vistas a preservar a sanidade das plantas, proteger o meio ambiente, reduzir os custos de produção e aumentar a rentabilidade do produtor. Para plantios de coqueiro as seguintes medidas podem ser consideradas:</p>

									<b>Medidas Profiláticas</b><br>

									<p>- O uso de mudas vigorosas, isentas de pragas e doenças <a data-toggle="modal" data-target="#tomada1" href="#"><strong>(clique aqui)</strong></a>, adquiridas de viveirista idôneo e com legitimidade, assegura seu maior pegamento após o plantio, bem como o pleno desenvolvimento da planta no campo e sua entrada mais cedo em produção.</p>

									<p>- Sementes provenientes de plantios de coqueiro-híbrido não servem para reprodução e são impróprias para o comércio de mudas. Quando esse tipo de material é plantado no campo, observa-se a desagregação dos genes, o que culmina em plantações desuniformes tanto em desenvolvimento quanto em produção.</p>

									<p>- A existência de formigueiros e cupinzeiros na área de plantio, se não for devidamente debelada durante o preparo da área, pode comprometer o cultivo do coqueiro.</p>

									<p>- O encoivaramento de materiais vegetais nas entrelinhas do plantio ou em locais vizinhos propicia a atração do adulto da broca-do-coleto Strategus aloeus, ao iniciar o processo de decomposição. As fêmeas atraídas fazem postura no material em decomposição, onde as larvas se desenvolvem e os adultos, ao emergirem, entram na plantação de coqueiro para se alimentar do coleto das plantas jovens, causando-lhes a morte.</p>

									<p>- O uso de casca de coco no fundo da cova de plantio pode ocasionar severas perdas na plantação se praticado em áreas com histórico de cupinzeiros.</p>

									<p>- O abandono de plantas mortas ou doentes dentro do plantio favorece a multiplicação de pragas e/ou de doenças e representa uma ameaça inclusive para as plantações vizinha, podendo transformar-se em sério problema de âmbito municipal, estadual ou regional.</p>

									<p>- Traças desenvolvem-se nas flores femininas e nos frutos novos do coqueiro causando queda prematura. As larvas completam seu ciclo no interior dos tecidos danificados e os adultos, após emergirem, voltam para realizar novas posturas nas inflorescências que se abrem. Flores e frutos que secam e permanecem presos nas inflorescências também servem de abrigo para várias espécies de traças. A coleta e a queima de todo material abortado ou caído prematuramente evitam que se transforme em focos de multiplicação das traças.</p>

									<b>Medidas Físicas</b><br>

									<p>- Armadilha luminosa é um instrumento que pode ser utilizado na plantação para monitorar ou para controlar população de pragas que demonstram atração pela luz. No coqueiro, a broca-do-coleto Strategus aloeus tem esse comportamento e por isso o uso dessa armadilha pode auxiliar na redução da infestação dessa praga. Vernard-Combes e Mariau (1983) recomendam o uso de armadilha luminosa como forma de controle de besouro Augosoma centauros, pertence à mesma família do S. aloeus.</p>

									<b>Medidas Mecânicas</b><br>

									<p>- Rede entomológica <a data-toggle="modal" data-target="#tomada2" href="#"><strong>(clique aqui)</strong></a> e armadilhas atrativas<a data-toggle="modal" data-target="#tomada3" href="#"><strong>(clique aqui)</strong></a> são meios alternativos utilizados na plantação, que permitem a eliminação direta de adultos, larvas, lagartas e de pupas ou crisálidas de pragas presentes na plantação. Servem tanto para monitorar a presença da praga quanto reduzir sua população, no campo. Operações como catação manual e poda de folhas secas infestadas também podem ser utilizadas visando a redução da população de pragas na plantação.</p>

									<p>- O fogo <a data-toggle="modal" data-target="#tomada4" href="#"><strong>(clique aqui)</strong></a> e o enterramento de tecidos broqueados (troncos, pedúnculos, ráquis, frutos e flores caídos e abortados) impedem a formação de focos de multiplicação de pragas.</p>
									 

									<b>Medidas Comportamentais</b><br>

									<p>- A utilização de armadilhas atrativas tornou-se a medida mais eficiente de monitoramento e de redução da população de espécies do gênero Rhynchophorus prejudiciais às palmáceas, nas diversas regiões do mundo. Em geral, as espécies desse gênero são atraídas por substâncias voláteis liberadas por palmeiras com ferimentos ou doentes e por outras fontes vegetais em processo de fermentação. Os mecanismos de comunicação envolvem tanto o comportamento do inseto quanto os componentes químicos presentes na planta (cairomone) e no inseto (feromônio). Estudos comprovam que os feromônios produzidos por espécies de gênero Rhynchophorus são de agregação, provocam estímulo no comportamento de machos e de fêmeas e são liberados pelo macho. Sozinho, o feromônio exerce pouca atratividade no inseto. No entanto, associado a pedaços de tecidos vegetais com poder de fermentação provoca um aumento significativo no número de insetos atraídos, comprovando a estreita interação existente entre os componentes químicos da planta e o feromônio produzido pelo inseto. Produto similar ao produzido pelos machos foi sintetizado em laboratório – feromônio sintético Rhynchophorol -  e hoje se encontra largamente utilizado em programas de manejo integrado da espécie R. palmurum, na América do Sul, e de outras espécies desse gênero, na África, Ásia, Oriente Médio e Oceania.</p>

									<p>- Armadilhas atrativas contendo o feromônio sintético + fontes vegetais (cana-de-açúcar, abacaxi, mamão, casca de coco-verde, inhame) são distribuídas na plantação para aprisionar os adultos de R. palmarum. Uma vez capturados nas armadilhas, os adultos são eliminados mecanicamente. Solução de melaço + água na proporção de 1:1 é utilizada no monitoramento da lagarta Opsyphanes invirae.</p>

									<p>- A utilização de iscas atrativas impregnadas com inseticidas é outra tática que pode ser utilizada em programas de controle de broca-do-olho do coqueiro, com base no comportamento dessa espécie (MOURA el al., 1990; 1998). Os adultos são atraídos pelos odores emanados das iscas e morrem ao pousar ou penetrar nas iscas quimicamente tratadas.</p>

									<b>Medidas Químicas</b><br>

									<p>- A pulverização química deve ser realizada de forma localizada, ou seja, dirigida para a parte da planta preferida pela praga em questão (raiz, caule, folhas – inferiores, intermediárias ou superiores, face superior ou inferior dos folíolos – frutos e flores) e para as áreas de maior infestação da praga. A grande questão está em selecionar o inseticida adequado e em definir como e quando aplica-lo. Deve-se dar preferência a inseticidas de baixo poder residual, seletivos e eficientes.</p>

									<p>- Poucos são os produtos registrados no Ministério da Agricultura, Pecuária e Abastecimento (MAPA) para uso na cultura do coqueiro (ver Grade de Agroquímicos). O clorofosforado triclorfon registrado no MAPA para controle de lagarta-desfolhadora, Brassolis sophorae foi retirado do mercado e não tem substituto. Os piretróides, apesar de eficientes no controle de algumas pragas de coqueiro, não são seletivos, não são registrados e podem destruir tanto insetos benéficos como inimigos naturais, motivo pelo qual recomenda-se certa cautela em seu uso e a orientação obrigatória de um especialista. Os carbamatos apresentam certa seletividade e eficiência, sendo o carbosulfan o único princípio ativo com registro no MAPA para a cultura do coqueiro.</p>

									<p>- Produtos que agem por contato e ingestão são aplicados via pulverização terrestre ou aérea. O conhecimento do hábito da praga é importante para a aplicação adequada do produto e garantia de maior eficiência. O mesmo é válido para o uso do equipamento adequado, que pode variar de um simples pulverizador costal <a data-toggle="modal" data-target="#tomada5" href="#"><strong>(clique aqui)</strong></a>, um costal motorizado <a data-toggle="modal" data-target="#tomada6" href="#"><strong>(clique aqui)</strong></a> um tanque tratorizado com pistolas de alta pressão e plataforma <a data-toggle="modal" data-target="#tomada7" href="#"><strong>(clique aqui)</strong></a> até um sofisticado avião <a data-toggle="modal" data-target="#tomada8" href="#"><strong>(clique aqui)</strong></a>, cuja escolha é condicionada pelo tamanho e pelo relevo da propriedade, pelo porte das plantas e pela natureza e nível de infestação da praga.</p>

									<p>- Produtos de ação sistêmica podem ser aplicados via pulverização ou via injeção no caule <a data-toggle="modal" data-target="#tomada9" href="#"><strong>(clique aqui)</strong></a> ou na raiz <a data-toggle="modal" data-target="#tomada10" href="#"><strong>(clique aqui)</strong></a>. A grande vantagem da técnica de injeção é a proteção que oferece ao meio ambiente, por preservar inimigos naturais e evitar contaminação no lençol freático, lagoas e rios, pelo fato do produto não ser depositado diretamente no solo. A grande desvantagem do uso de produtos sistêmicos em coqueiro são os resíduos que deixa no fruto (no albúmen sólido e na água de coco). Uma carência mínima de 90 dias é necessária após o tratamento da planta via raiz e de 30 dias após a útima pulverização aérea, como garantia de que os componentes do fruto estejam livres de resíduos químicos.</p>

									<p>- O melhor horário para fazer a pulverização é pela manhã e no final da tarde, sempre nas horas amenas do dia.</p>

									<p>- As pulverizações devem ser feitas a favor do vento para evitar casos de intoxicação.</p>

									<p>- É obrigatório o uso dos equipamentos de proteção individual (E.P.I) antes, durante e após o manuseio e a aplicação de produtos químicos.</p>

									<p>- A presença de uma espécie pode ser tolerada na plantação sem que haja comprometimento da produção. Um nível baixo de infestação pode representar fator importante de regulação das populações de insetos benéficos. A prática do monitoramento da plantação (Inspeção de Rotina e/ou Inspeção Especial) permite determinar o nível de dano da praga a partir do qual as perdas sofridas representem prejuízos econômicos à plantação justificando a adoção de medidas químicas de controle.</p>

									<b>Medidas Biológicas</b><br>

									<p>- Preservação de plantas silvestres nas entrelinhas de plantio do coqueiro. Estas, por sua diversidade, oferecem massa vegetal, néctar e pólen em quantidade suficiente para atrair, manter e alimentar os insetos entomófagos (predadores e parasitóides) que regulam, no ambiente, a população de insetos fitófatos e ajudam a manter essas populações em equilíbrio. O uso constante de tratos culturais na plantação, como a aplicação de herbicidas e a realização de campinas, concorre para a eliminação da fonte de ferrogeamento natural dessas espécies, levando muitos parasitoides e predadores a procurar outros sítios para se alimentar e multiplicar. Da mesma forma, o uso indiscriminado e inadequado de agrotóxicos provoca a redução/eliminação da população da fauna benéfica, favorecendo o aumento da população da praga.</p>

									<p>- Reconhecimento dos inimigos naturais (predadores, parasitoides e patógenos) das pragas que causam danos econômicos à plantação. A entomofauna da cultura do coqueiro é rica em inimigos naturais <a data-toggle="modal" data-target="#tomada11" href="#"><strong>(veja tabela)</strong></a>.</p>

									<p>- Produção em grande quantidade e com custo reduzido de inimigos naturais com potencial de uso e de mercado.</p>

									<b>Medidas Legislativas</b><br>

									<p>- Risco de introdução de pragas e doenças em áreas novas de cultivo pelo transporte de mudas. Qualquer trânsito de material vegetal entre estados, regiões ou países deve ser rigorosamente fiscalizado, principalmente quando procedente de áreas de comprovada ocorrência de uma praga-chave e/ou doença. Para isso, é fundamental a atuação de barreiras fitossanitárias e a exigência, pelo adquirente, do certificado fitossanitário no ato de recebimento do material, emitido pelo Serviço de Defesa Fitossanitária do exportador.</p>

									<p>- No ato do recebimento das mudas na plantação e/ou no porto de entrada do país, é válido proceder à aplicação de tratamento químico com produto de largo espectro, para evitar possível introdução de pragas ou de insetos vetores de doenças, nos estágios de ovo, larva, pupa e/ou adulto. Uma espécie introduzida em novo ambiente tem capacidade de se propagar gradativa ou rapidamente e vir a causar sérios prejuízos à lavoura, em virtude de estar livre de pressão exercida por seus inimigos naturais.</p>

									<b>Medidas Proibitivas</b><br>

									<p>- Corte de folhas verdes em coqueiro. Durante o processo de senescência da folha, os nutrientes são naturalmente carreados para as folhas mais novas. Cortar a folha ainda verde é interferir no processo de translocação e de reaproveitamento dos nutrientes pela planta, e em sua produção, em virtude da redução da área foliar. Vale ressaltar que, pela lesão feita nos tecidos da base da folha são liberados constituintes químicos (cairomones) que atraem pragas, como a broca-do-olho Rhynchophorus palmarum, cujo adulto é responsável por perdas na plantação por transmitir o nematoide causador da doença letal anel-vermelho.</p>
										
										
